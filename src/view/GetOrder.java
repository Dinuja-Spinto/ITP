    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String SupplierId=jTextField6.getText();
        String Name=jTextField2.getText();
        String Address=jTextField4.getText();
        String mobileNumber=jTextField5.getText();
        String Email=jTextField7.getText();
        
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("insert into GetOrder values('"+SupplierId+"','"+Name+"','"+Address+"','"+mobileNumber+"','"+Email+"')");
            JOptionPane.showMessageDialog(null,"Sucessfully Added");
            setVisible(false);
            new GetOrder().setVisible(true);
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,"Send the your message!!!");
        }
        
        
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

